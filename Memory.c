////////////////////////////////////////////////////////////////////////////////
//
//   Name:          MemCopy
//
//   Type:          Function
//
//   Description:   To copy the content of one memory location to another
//
//   Inputs:        source - the source memory location
//                  destination - the destination memory location
//
//   Outputs:       void
//
////////////////////////////////////////////////////////////////////////////////
void MemCopy(void *source, void *dest, int count)
{
    unsigned char *src = (unsigned char *)source;
    unsigned char *dst = (unsigned char *)dest;
    int i = 0;

    if (source == NULL || dest == NULL)
        return;

    while (i < count)
    {
        dst[i] = src[i];
        i++;
    }
}

////////////////////////////////////////////////////////////////////////////////
//
//   Name:          MemSet
//
//   Type:          Function
//
//   Description:   To set the content of a memory location
//
//   Inputs:        target - the target memory location
//                  n - the value to set
//                  count - how many bytes to set
//
//   Outputs:       void
//
////////////////////////////////////////////////////////////////////////////////
void MemSet(void *target, int n, int count)
{
    unsigned char *tgt = (unsigned char *)target;
    int i = 0;

    if (target == NULL)
        return;

    while (i < count)
    {
        tgt[i] = (unsigned char)n;
        i++;
    }
}
